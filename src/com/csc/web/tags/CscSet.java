//com.csc.turtleblog.tags.HistoryTagSupport
public class CscSet extends CscGet
{
	//-------------------------
	//numbers
	//-------------------------
	private static final long serialVersionUID = 1L;
	//****************************************************************************************************
	//Begin - Constructor
	//****************************************************************************************************
	public CscSet()
	{
	}
	//****************************************************************************************************
	//End  - Constructor
	//****************************************************************************************************
	//****************************************************************************************************
	//Begin  - Get/Set Methods
	//****************************************************************************************************
	private String _value;
	public void setValue(String value) {_value=value;}
	//****************************************************************************************************
	//End - Get/Set Methods
	//****************************************************************************************************		
	//****************************************************************************************************
	//Begin  - Public Methods
	//****************************************************************************************************
	public int doStartTag()
	{
	    Hashtable<String, String> _properties = getProperties();
	    
	    if(_name!=null&&_value!=null)_properties.put(_name, _value);
		
	    int _returnValue = _id==null?SKIP_BODY:EVAL_BODY_INCLUDE;
	    
		return _returnValue;
		
	}
	public int doEndTag()
	{
		try {pageContext.include(_id);}
		
		catch(Exception ex){}
		
		return EVAL_PAGE;		
	}
	//****************************************************************************************************
	//End - Public Methods
	//****************************************************************************************************	
}
